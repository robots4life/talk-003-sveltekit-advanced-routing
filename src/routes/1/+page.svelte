<h1 class="pt-20 font-bold">What Is Routing ?</h1>

<p>
	SvelteKit uses filesystem-based routing, which means that the routes of your app — in other words,
	what the app should do when a user navigates to a particular URL — are defined by the directories
	in your codebase.
</p>

<p>Every +page.svelte file inside src/routes creates a page in your app.</p>

